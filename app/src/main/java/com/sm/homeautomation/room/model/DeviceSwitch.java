package com.sm.homeautomation.room.model;

public class DeviceSwitch {
    private int pwr;
    private int time;
    private int trig;

    public int getPwr() {
        return pwr;
    }

    public void setPwr(int pwr) {
        this.pwr = pwr;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getTrig() {
        return trig;
    }

    public void setTrig(int trig) {
        this.trig = trig;
    }
}
